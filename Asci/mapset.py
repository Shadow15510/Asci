def get_map(n, outdoor):
    if outdoor:
        if n == 1:
            return r""" 
 ###  $   __     __
#####    /<>\___/<>\
 ###     |  |   |  |
 /_\     |  | _ |  |
         |___|^|___|
|==|==|
"""

        elif n == 2:
            return r"""
 __            ####
/  \__        ######
|_    \       ######
|^|___|       ######
               ####
                ||
"""
    
        elif n == 3:
            return r"""
\          _
\\        / \_
\\|==|==|=|_  \
 \|==|==|=|^|_|==|==|
        
        
"""
    
        elif n == 4:
            return r"""
 ###   ###   ###
##### #####*#####
 ###   ###   ###
 /_\   /_\   /_\\
       $             
|==|==|==|==|==|  |=
"""
    
        elif n == 5:
            return r"""
   _   _    ###   ###
  / \_/ \  ##### #####
  |_    |   ###   ###
  |^|___|   /_\   /_\
 
|~~|~~|~~|~~|~~|~~|~~
"""

        elif n == 6:
            return r"""
~~~~~~~~     _    _
~~~~~~  *   / \  / \
~~~~~~~     |_|  |_|
~~~~~~~~~         _
~~~~             / \
~~~~~~~~         |_|
"""

        elif n == 7:
            return r"""
 _    *
/ \_    _   _
|_  \  / \ / \  _
|^|_|  |_| |_| / \
               |_|
$
"""

        elif n == 8:
            return r"""
  $    ###   _
 ###  ##### / \
#####  ###  |_|  *
 ###   /_\        _ 
 /_\             / \
                 |_|
"""
    
    else:
        if n == 1:
            return r"""
+-------------------+
|              +--+ |
|  *           |~~| |
|              +--+ |
|                   |
+-------|  |--------+
"""

        elif n == 2:
            return r"""
+---------------+---+
|               |*  |
|  +----------- |   |
+- |              --+
|     |       |     |
+-----+-|  |--+-----+
"""

        elif n == 3:
            return r"""
+-------+--+--------+
| *     |  |*       |
|  -----+  +----  --+
|                   |
|  |    |  |        |
+--+----|  |--------+
"""

        elif n == 5:
            return r"""
+-------------------+
|=====/======\======|
|=====/      \==|_|=|
|==/            |_| |
| *             |_| |
+-------|  |--------+
"""

        elif n == 7:
            return r"""
+-------+--+--------+
|       |  |        |
+--  ---+         * |
|          |        |
|       |  |        |
+-------|  |--------+
"""
